import numpy as np

# GCD / HCF Operations

# GCD of two numbers
print("GCD of 12 & 18:", np.gcd(12, 18))


# GCD of two arrays
a = np.array([12, 15, 21])
b = np.array([18, 25, 14])
print("GCD of arrays a & b:", np.gcd(a, b))


# LCM Operations

# LCM of two numbers
print("LCM of 12 & 18:", np.lcm(12, 18))

# LCM of two arrays
c = np.array([4, 6, 12])
d = np.array([10, 15, 18])
print("LCM of arrays c & d:", np.lcm(c, d))

